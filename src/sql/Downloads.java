package sql;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Lob;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author Patrick
 */
@Entity
@Table(name = "downloads")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Downloads.findAll", query = "SELECT d FROM Downloads d"),
    @NamedQuery(name = "Downloads.findById", query = "SELECT d FROM Downloads d WHERE d.id = :id"),
    @NamedQuery(name = "Downloads.findByPid", query = "SELECT d FROM Downloads d WHERE d.pid = :pid"),
    @NamedQuery(name = "Downloads.findByName", query = "SELECT d FROM Downloads d WHERE d.name = :name"),
    @NamedQuery(name = "Downloads.findByHits", query = "SELECT d FROM Downloads d WHERE d.hits = :hits")})
public class Downloads implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Column(name = "pid")
    private Integer pid;
    @Column(name = "name")
    private String name;
    @Lob
    @Column(name = "description")
    private String description;
    @Lob
    @Column(name = "link")
    private String link;
    @Lob
    @Column(name = "image")
    private String image;
    @Column(name = "hits")
    private Integer hits;

    public Downloads() {
    }

    public Downloads(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getPid() {
        return pid;
    }

    public void setPid(Integer pid) {
        this.pid = pid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public Integer getHits() {
        return hits;
    }

    public void setHits(Integer hits) {
        this.hits = hits;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Downloads)) {
            return false;
        }
        Downloads other = (Downloads) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "worldwide.airline.route.editor.Downloads[ id=" + id + " ]";
    }
    
}
